package com.shanlinjinrong.oa.ui.activity.home.schedule.initiateapproval.bean;

public class SubmitRequestBean {


    private int code;

    private String beginTime;

    private String endTime;

    private String requestType;

    private String requestDate;

    private String requestYear;

    private String requestDuration;

    private String requestMatter;

    private String requestRecipient;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getBeginTime() {
        return beginTime;
    }

    public void setBeginTime(String beginTime) {
        this.beginTime = beginTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    public String getRequestType() {
        return requestType;
    }

    public void setRequestType(String requestType) {
        this.requestType = requestType;
    }

    public String getRequestDate() {
        return requestDate;
    }

    public void setRequestDate(String requestDate) {
        this.requestDate = requestDate;
    }

    public String getRequestYear() {
        return requestYear;
    }

    public void setRequestYear(String requestYear) {
        this.requestYear = requestYear;
    }

    public String getRequestDuration() {
        return requestDuration;
    }

    public void setRequestDuration(String requestDuration) {
        this.requestDuration = requestDuration;
    }

    public String getRequestMatter() {
        return requestMatter;
    }

    public void setRequestMatter(String requestMatter) {
        this.requestMatter = requestMatter;
    }

    public String getRequestRecipient() {
        return requestRecipient;
    }

    public void setRequestRecipient(String requestRecipient) {
        this.requestRecipient = requestRecipient;
    }
}
